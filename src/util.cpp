void ToUpper(char *str, int len){
	int i=0;
	for(i=0; i < len; i++){
		if(islower(str[i])){ str[i] = toupper(str[i]);}
	}
}

/////////////////////////////////////////////////////////
void StringToUpper(std::string & data){
	std::transform(data.begin(), data.end(), data.begin(), ::toupper);
}

